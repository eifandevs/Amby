import Foundation
import Entity
import RxCocoa
import RxSwift

public final class AddTabUseCase {

    private var tabDataModel: TabDataModelProtocol!

    public init() {
        setupProtocolImpl()
    }

    private func setupProtocolImpl() {
        tabDataModel = TabDataModel.s
    }

    /// タブの追加
    public func exe(url: String? = nil) {
        tabDataModel.append(url: url, title: nil)
    }
}
